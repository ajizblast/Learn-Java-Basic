public class OPerasiMatematika {

    public static void main(String[] args) {

        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        //AUGMENTED ASIGNMENT

        int c = 100;

        c += 10;
        System.out.println(c);
        c -= 10;
        System.out.println(c);
        c *= 10;
        System.out.println(c);

        //UNARY OPERATOR
        int d = 100;

        d++;
        System.out.println(d);
        d--;
        System.out.println(d);
    }
}
